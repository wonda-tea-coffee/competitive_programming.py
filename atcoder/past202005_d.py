A = [
    ["###","#.#","#.#","#.#","###"],
    [".#.","##.",".#.",".#.","###"],
    ["###","..#","###","#..","###"],
    ["###","..#","###","..#","###"],
    ["#.#","#.#","###","..#","..#"],
    ["###","#..","###","..#","###"],
    ["###","#..","###","#.#","###"],
    ["###","..#","..#","..#","..#"],
    ["###","#.#","###","#.#","###"],
    ["###","#.#","###","..#","###"],
]
N = int(input())
S = [input() for _ in range(5)]
i = 1
j = 0
ans = []
while j < N:
    for k in range(10):
        res = True
        for l in range(5):
            res &= A[k][l][0] == S[l][i] and A[k][l][1] == S[l][i+1] and A[k][l][2] == S[l][i+2]

        if res:
            ans.append(k)
            break

    i += 4
    j += 1

print("".join(map(str, ans)))
